package com.practice;

public class PrettyPrinting_04 {
    public static void main(String[] args) {
        float a = 432.1234f;
        System.out.printf("Formatted number is : %.2f", a);
        System.out.println();
        System.out.printf("Pie : %.3f", Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s and i'm %s", "Chiran", "Cool");
    }
}
